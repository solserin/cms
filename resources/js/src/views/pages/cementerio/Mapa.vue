<script>
import cementerio from "@services/cementerio";
export default {
  props: {
    idAreaInicial: {
      type: Number,
      default: 0,
    },
    disabled: {
      required: false,
      type: Boolean,
      default: false,
    },
  },

  watch: {
    idAreaInicial: function (newValue, oldValue) {
      if (newValue > 0) {
        this.id_area_activa = newValue;
      } else {
        this.status_propiedadades = [];
        this.id_area_activa = 0;
      }
    },
    id_area_activa: function (newValue, oldValue) {
      if (newValue > 0) {
        (async () => {
          await this.get_cementerio();
        })();
      }
    },
  },
  components: {},
  data() {
    return {
      propiedades: [],
      //todas las status_propiedadades
      status_propiedadades: [],
      id_area_activa: 0,
      datosAreaSeleccionada: [],
    };
  },
  computed: {
    area_inicial: {
      get() {
        return this.idAreaInicial;
      },
      set(newValue) {
        return newValue;
      },
    },
    deshabilitado: {
      get() {
        return this.disabled;
      },
      set(newValue) {
        return newValue;
      },
    },
  },
  methods: {
    async get_cementerio() {
      this.$vs.loading();
      this.datosAreaSeleccionada = [];
      try {
        let res = await cementerio.get_cementerio();
        this.propiedades = res.data;
        if (this.id_area_activa > 0) {
          this.datosAreaSeleccionada = this.propiedades[
            this.id_area_activa - 1
          ];
        }
        this.status_propiedadades.push("dato vacio");
        res.data.forEach((element) => {
          if (element.status == 1) {
            this.status_propiedadades.push("disponible");
          } else {
            //no esta activo y no se puede vender de ahi
            this.status_propiedadades.push("bloqueado");
          }
        });
        //mando los datos al parent
        this.$emit("getDatosTipoPropiedad", this.datosAreaSeleccionada);
        this.$vs.loading.close();
      } catch (error) {
        alert("ocurriÃ³ un error, repÃ³rtelo a sistemas.");
        this.$vs.loading.close();
      }
    },
    handleSearch(searching) {},
    handleChangePage(page) {},
    handleSort(key, active) {},
    seleccionarArea(id_area) {
      if (!this.deshabilitado) {
        this.id_area_activa = id_area;
      } else {
        this.$emit("respuestaDeshabilitado");
      }
    },
  },
  created() {
    // this.id_area_activa = this.area_inicial;
  },
};
</script>

